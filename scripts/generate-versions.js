const fs = require('node:fs');
const path = require('node:path');

const pkg = JSON.parse(fs.readFileSync(path.join(__dirname, '..', 'package.json'), 'utf8'));
const deps = { ...pkg.dependencies, ...pkg.devDependencies };

const versions = {};
for (const [name, range] of Object.entries(deps)) {
  versions[name] = range.replace(/^[\^~]/, '').replace(/\.\d+$/, '');
}

const out = `// Auto-generated by scripts/generate-versions.js — do not edit
export const versions: Record<string, string> = ${JSON.stringify(versions, null, 2)};
`;

const outPath = path.join(__dirname, '..', 'generated', 'versions.ts');
const existing = fs.existsSync(outPath) ? fs.readFileSync(outPath, 'utf8') : '';
if (existing !== out) {
  fs.writeFileSync(outPath, out);
}
